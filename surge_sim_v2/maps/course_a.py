"""L字型サンプルコース。

直角コーナー1箇所を持つ L字型の閉ループ、コース幅 1m。
外周 L字ポリゴンと内周 L字ポリゴンの間がコース。座標単位 [m]。
"""

COURSE_NAME = "L-Shape Course"


def _poly_edges(points):
    """頂点列を閉ループの線分リストに変換する。"""
    n = len(points)
    return [(points[i], points[(i + 1) % n]) for i in range(n)]


# 外周 L字（左下→反時計回り）。右上 2x2 を欠いた L 形状。
_OUTER = [
    (0.0, 0.0), (5.0, 0.0), (5.0, 3.0),
    (3.0, 3.0), (3.0, 5.0), (0.0, 5.0),
]

# 内周 L字（各辺から 1m 内側 = コース幅 1m）
_INNER = [
    (1.0, 1.0), (4.0, 1.0), (4.0, 2.0),
    (2.0, 2.0), (2.0, 4.0), (1.0, 4.0),
]

WALLS = _poly_edges(_OUTER) + _poly_edges(_INNER)

# 下側コーナーからスタート、東向き
START_POSE = (0.5, 0.5, 0.0)

# Phase2 用カンニング中心線（コーナーを回る閉ループ）
CENTER_LINE = [
    (0.5, 0.5), (4.5, 0.5), (4.5, 2.5), (2.5, 2.5), (2.5, 4.5), (0.5, 4.5),
]
